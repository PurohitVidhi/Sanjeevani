    private void loadEmloyeeDetails() {
        try {
            List<EmpPojo> empList = EmpDao.getAllEmployeeDetails();
            if (empList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No record found!", "Oops!", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            for (EmpPojo e : empList) {
                Vector<String> v = new Vector<>();
                v.add(e.getEmpId());
                v.add(e.getEmpName());
                v.add(e.getEmpDept());
                v.add(String.valueOf((e.getEmpSal())));
                model.addRow(v);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error In DB" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
